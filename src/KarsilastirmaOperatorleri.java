public class KarsilastirmaOperatorleri {
    public static void main (String[] args) {
        int a = 20;
        int b = 10;
        // eşit mi?
        System.out.println(a == b);
        // eşit değil mi?
        System.out.println(a != b);
        // buyuk mu?
        System.out.println(a>b);
        // kucuk mu?
        System.out.println(a<b);
        // buyuk esit mi?
        System.out.println(a>=b);
    }
}
