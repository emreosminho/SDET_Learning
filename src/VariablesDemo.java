
public class VariablesDemo {

	public static void main(String[] args) {
		// Tam sayı tipleri
		int sayi = 10;
		long buyukSayi = 100000L;
		
		// Ondalıklı veri tipleri
		double ondalik = 5.15;
		float kucukOndalik = 3.14f;
		
		// Karakter ve String
		char karakter = 'A';
		String mesaj = "Merhaba Java";
		
		// Boolean
		boolean aktifMi = true;
		
		
		
		
		System.out.println("Sayi: " + sayi);
		System.out.println("Buyuk Sayi: " + buyukSayi);
		System.out.println("Ondalik: " + ondalik);
		System.out.println("Kucuk Ondalik: " + kucukOndalik);
		System.out.println("Karakter: " + karakter);
		System.out.println("String deger: " + mesaj);
		System.out.println("Aktif Mi?: " + aktifMi);
		

	}

}
